// ── Mechanic Link Card ────────────────────────────────────────────────────────

@Composable
fun MechanicLinkCard(
    expiry:     String,
    expiryOpts: List<String>,
    onExpiry:   (String) -> Unit,
    generated:  Boolean,
    onGenerate: () -> Unit,
) {
    var dropdownOpen by remember { mutableStateOf(false) }

    ActyCard(
        modifier = Modifier
            .fillMaxWidth()
            .padding(horizontal = 20.dp),
    ) {
        Column(Modifier.padding(20.dp)) {
            Row(verticalAlignment = Alignment.CenterVertically) {
                Box(
                    modifier = Modifier
                        .size(40.dp)
                        .clip(RoundedCornerShape(12.dp))
                        .background(StatusBlueBg),
                    contentAlignment = Alignment.Center,
                ) {
                    Icon(Icons.Outlined.Link, null, tint = StatusBlue, modifier = Modifier.size(22.dp))
                }
                Spacer(Modifier.width(12.dp))
                Column {
                    Text(
                        "Mechanic Link",
                        style = MaterialTheme.typography.titleMedium.copy(color = TextPrimary),
                    )
                    Text(
                        "Read-only, time-limited, no account required",
                        style = MaterialTheme.typography.bodySmall,
                    )
                }
            }

            Spacer(Modifier.height(14.dp))

            Row(
                modifier              = Modifier.fillMaxWidth(),
                verticalAlignment     = Alignment.CenterVertically,
                horizontalArrangement = Arrangement.SpaceBetween,
            ) {
                Text("Link expires in:", style = MaterialTheme.typography.bodyMedium.copy(color = TextSecondary))
                Box {
                    Row(
                        modifier = Modifier
                            .shadow(3.dp, RoundedCornerShape(100.dp), spotColor = Color.Black.copy(alpha = 0.05f))
                            .clip(RoundedCornerShape(100.dp))
                            .background(Color.White)
                            .border(0.5.dp, BgBorder, RoundedCornerShape(100.dp))
                            .clickable { dropdownOpen = true }
                            .padding(horizontal = 12.dp, vertical = 8.dp),
                        verticalAlignment = Alignment.CenterVertically,
                    ) {
                        Text(expiry, style = MaterialTheme.typography.labelMedium.copy(color = StatusBlue))
                        Spacer(Modifier.width(4.dp))
                        Icon(Icons.Filled.ArrowDropDown, null, tint = TextDim, modifier = Modifier.size(16.dp))
                    }
                    DropdownMenu(
                        expanded         = dropdownOpen,
                        onDismissRequest = { dropdownOpen = false },
                        modifier         = Modifier.background(Color.White),
                    ) {
                        expiryOpts.forEach { opt ->
                            DropdownMenuItem(
                                text    = { Text(opt, style = MaterialTheme.typography.bodyMedium.copy(color = TextPrimary)) },
                                onClick = { onExpiry(opt); dropdownOpen = false },
                            )
                        }
                    }
                }
            }

            Spacer(Modifier.height(14.dp))

            if (generated) {
                Box(
                    modifier = Modifier
                        .fillMaxWidth()
                        .shadow(3.dp, RoundedCornerShape(12.dp), spotColor = StatusBlue.copy(alpha = 0.10f))
                        .clip(RoundedCornerShape(12.dp))
                        .background(StatusBlueBg)
                        .border(0.5.dp, StatusBlue.copy(alpha = 0.25f), RoundedCornerShape(12.dp))
                        .padding(12.dp),
                ) {
                    Column {
                        Text("Mechanic link ready", style = MaterialTheme.typography.titleSmall.copy(color = StatusBlue))
                        Spacer(Modifier.height(4.dp))
                        Text(
                            text  = "https://verify.acty-labs.com/m/abc123xyz",
                            style = MaterialTheme.typography.bodySmall.copy(color = TextPrimary),
                        )
                        Spacer(Modifier.height(8.dp))
                        Row(horizontalArrangement = Arrangement.spacedBy(8.dp)) {
                            ShareActionChip(icon = Icons.Outlined.ContentCopy, label = "Copy",  onClick = {})
                            ShareActionChip(icon = Icons.Outlined.Share,       label = "Share", onClick = {})
                        }
                    }
                }
            } else {
                Box(
                    modifier = Modifier
                        .fillMaxWidth()
                        .shadow(4.dp, RoundedCornerShape(14.dp), spotColor = StatusBlue.copy(alpha = 0.15f))
                        .clip(RoundedCornerShape(14.dp))
                        .background(Brush.linearGradient(listOf(CactusBlue, CactusBlueMid)))
                        .clickable(onClick = onGenerate)
                        .padding(vertical = 14.dp),
                    contentAlignment = Alignment.Center,
                ) {
                    Row(
                        verticalAlignment     = Alignment.CenterVertically,
                        horizontalArrangement = Arrangement.spacedBy(8.dp),
                    ) {
                        Icon(Icons.Outlined.Link, null, tint = Color.White, modifier = Modifier.size(18.dp))
                        Text(
                            "Generate Mechanic Link",
                            style = MaterialTheme.typography.titleSmall.copy(
                                color      = Color.White,
                                fontWeight = FontWeight.SemiBold,
                            ),
                        )
                    }
                }
            }
        }
    }
}

// ── Social Share Row ──────────────────────────────────────────────────────────

@Composable
fun SocialShareRow() {
    val platforms = listOf(
        Triple(Icons.Outlined.Share,   "Share",   CactusBlue),
        Triple(Icons.Outlined.Message, "SMS",     StatusGreenDim),
        Triple(Icons.Outlined.Email,   "Email",   StatusBlue),
        Triple(Icons.Outlined.Forum,   "Discord", AccentPurple),
    )

    Row(
        modifier              = Modifier
            .fillMaxWidth()
            .padding(horizontal = 20.dp),
        horizontalArrangement = Arrangement.spacedBy(10.dp),
    ) {
        platforms.forEach { (icon, label, color) ->
            SocialChip(icon = icon, label = label, color = color, modifier = Modifier.weight(1f))
        }
    }
}

@Composable
fun SocialChip(icon: ImageVector, label: String, color: Color, modifier: Modifier = Modifier) {
    Box(
        modifier = modifier
            .shadow(4.dp, RoundedCornerShape(14.dp), spotColor = color.copy(alpha = 0.10f))
            .clip(RoundedCornerShape(14.dp))
            .background(Color.White)
            .border(0.5.dp, color.copy(alpha = 0.15f), RoundedCornerShape(14.dp))
            .clickable {}
            .padding(vertical = 14.dp),
        contentAlignment = Alignment.Center,
    ) {
        Column(
            horizontalAlignment = Alignment.CenterHorizontally,
            verticalArrangement = Arrangement.spacedBy(6.dp),
        ) {
            Box(
                modifier = Modifier
                    .size(32.dp)
                    .clip(RoundedCornerShape(10.dp))
                    .background(color.copy(alpha = 0.08f)),
                contentAlignment = Alignment.Center,
            ) {
                Icon(icon, null, tint = color, modifier = Modifier.size(18.dp))
            }
            Text(label, style = MaterialTheme.typography.labelSmall.copy(color = TextSecondary))
        }
    }
}

