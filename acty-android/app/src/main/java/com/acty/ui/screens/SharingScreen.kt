// ── SharingScreen ─────────────────────────────────────────────────────────────

@Composable
fun SharingScreen(sessionId: String, onBack: () -> Unit) {
    var mechanicLinkGenerated by remember { mutableStateOf(false) }
    var mechanicExpiry        by remember { mutableStateOf("24 hours") }
    val expiryOptions = listOf("2 hours", "24 hours", "7 days", "30 days")

    Column(
        modifier = Modifier
            .fillMaxSize()
            .background(
                Brush.verticalGradient(
                    colors = listOf(StatusBlueBg, Color(0xFFF8FAFC)),
                    startY = 0f, endY = 600f,
                )
            )
            .verticalScroll(rememberScrollState())
            .systemBarsPadding()
            .padding(bottom = 32.dp),
    ) {
        SharingHeader(sessionId = sessionId, onBack = onBack)

        Spacer(Modifier.height(20.dp))

        SectionHeader(title = "Verified Report")
        Spacer(Modifier.height(10.dp))
        VerifiedReportCard(sessionId = sessionId)

        Spacer(Modifier.height(20.dp))

        SectionHeader(title = "Mechanic Link")
        Spacer(Modifier.height(10.dp))
        MechanicLinkCard(
            expiry     = mechanicExpiry,
            expiryOpts = expiryOptions,
            onExpiry   = { mechanicExpiry = it },
            generated  = mechanicLinkGenerated,
            onGenerate = { mechanicLinkGenerated = true },
        )

        Spacer(Modifier.height(20.dp))

        SectionHeader(title = "Share Report")
        Spacer(Modifier.height(10.dp))
        SocialShareRow()

        Spacer(Modifier.height(20.dp))

        SectionHeader(title = "Export")
        Spacer(Modifier.height(10.dp))
        ExportOptionsCard()
    }
}

// ── Sharing Header ────────────────────────────────────────────────────────────

@Composable
fun SharingHeader(sessionId: String, onBack: () -> Unit) {
    Box(
        modifier = Modifier
            .fillMaxWidth()
            .padding(horizontal = 20.dp, vertical = 20.dp),
    ) {
        Column {
            Row(verticalAlignment = Alignment.CenterVertically) {
                Box(
                    modifier = Modifier
                        .size(36.dp)
                        .shadow(3.dp, RoundedCornerShape(10.dp), spotColor = Color.Black.copy(alpha = 0.06f))
                        .clip(RoundedCornerShape(10.dp))
                        .background(Color.White)
                        .border(0.5.dp, BgBorder, RoundedCornerShape(10.dp))
                        .clickable(onClick = onBack),
                    contentAlignment = Alignment.Center,
                ) {
                    Icon(Icons.Filled.ArrowBack, "Back", tint = TextSecondary, modifier = Modifier.size(18.dp))
                }
                Spacer(Modifier.width(12.dp))
                Text(
                    text  = "Share Session",
                    style = MaterialTheme.typography.headlineMedium.copy(
                        fontWeight = FontWeight.Black,
                        color      = TextPrimary,
                    ),
                )
            }
            Spacer(Modifier.height(6.dp))
            Text(
                text  = sessionId.removeSuffix(".csv").removePrefix("acty_obd_"),
                style = MaterialTheme.typography.bodySmall.copy(color = TextDim),
                modifier = Modifier.padding(start = 48.dp),
            )
        }
    }
}

